// the major leagues; national flags come from flagcdn (every ISO code).
//
// Club crests: a.espncdn.com/i/teamlogos/soccer/500/<id>.png
// National flags: flagcdn.com/w160/<iso>.png
//
// Clubs ESPN doesn't host a crest for carry logoUrl == null; the picker
// falls back to a colored monogram badge. The pickers are searchable,
// so long lists are fine.
// ─────────────────────────────────────────────────────────────────

